const cart = {
  items: [],
  getItems() {
    return this.items
  },
  add(product) {
    // console.table(this.items); 

    for (const item of this.items) {
      if (item.name === product.name) {
        // console.log(quantity);
        item.quantity += 1;
        return;
      }
    }

    const newProduct = {
      ...product,
      quantity: 1,
    };
     
    this.items.push(newProduct)
  },
  remove(productName) {
    const { items } = this;

    for (let i = 0; i < items.length; i += 1) {
    const { name, price } = items[i];
      // console.log(`${name}: ${price} (${i})`);
  
      if (productName === name) {
        // console.log(`–ù–∞—à–ª–∏ —ç–ª–µ–º–µ–Ω—Ç –∫–æ—Ç–æ—Ä—ã –Ω—É–∂–Ω–æ —É–¥–∞–ª–∏—Ç—å: ${productName}: ${price} (${i})`);

        items.splice(i, 1)
      }
}

  },

  clear() {
    this.items = [];
  },

  countTotalPrice() {
    const { items } = this;
    // console.log(items);
    let total = 0;
    for (const { price, quantity } of items) {
      // console.log(price);
      total += price * quantity;
    }
    return total
  },

  increaseQuantity(productName) {
    for (const item of this.items) {
      if (item.name === productName) {
        item.quantity += 1;
        return;
      }
    }
  },
  decreaseQuantity(productName) {
    for (const item of this.items) {
      if (item.name === productName) {
        item.quantity -= 1;
        return;
      }
    }
  
  },
};

